def запитай_з_графом(питання: str) -> str:
    """
    RAG функція:
    1. Шукає контекст у Knowledge Graph
    2. Формує промпт: контекст + питання
    3. Надсилає до LLM
    4. Повертає відповідь

    Якщо контекст не знайдено — LLM про це говорить чесно.
    """
    # Крок 1: Пошук у графі
    контекст = знайди_контекст(питання)

    # Крок 2: Формуємо системний промпт
    if контекст:
        системний = f"""Ти — асистент який відповідає ТІЛЬКИ на основі наданого контексту.
Якщо відповідь не можна знайти у контексті — скажи "У базі знань немає інформації про це".
Не вигадуй факти яких немає у контексті.

{контекст}"""
    else:
        системний = """Ти — асистент який відповідає ТІЛЬКИ на основі бази знань.
Для цього питання у базі знань немає інформації. Скажи про це чесно."""

    # Крок 3: Надсилаємо до LLM
    response = client.chat.completions.create(
        model=DEFAULT_MODEL,
        messages=[
            {"role": "system", "content": системний},
            {"role": "user", "content": питання}
        ],
        temperature=0.3,  # нижча температура = менше "творчості" = менше галюцинацій
        max_tokens=500,
    )

    відповідь = response.choices[0].message.content

    # Крок 4: Показуємо що використали як контекст
    if контекст:
        print(f"\n[Знайдено контекст у графі]")
    else:
        print(f"\n[Контекст у графі не знайдено]")

    return відповідь


