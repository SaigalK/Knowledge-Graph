def знайди_контекст(запит: str) -> str:
    """
    Шукає у Knowledge Graph концепти пов'язані із запитом.
    Повертає текст-контекст для LLM.

    Логіка:
    1. Ділимо запит на слова
    2. Для кожного слова шукаємо концепти у графі
    3. Збираємо: яке слово → які концепти → з яких документів
    4. Формуємо текстовий контекст для LLM
    """
    слова = запит.lower().split()
    знайдено = {}  # {документ: [концепти]}

    for слово in слова:
        if len(слово) < 3:  # пропускаємо короткі слова типу "і", "в", "на"
            continue
        if "'" in слово or "'" in слово:  # пропускаємо слова з апострофом
            continue

        safe_слово = слово.replace("'", "''")
        result = conn.execute(f"""
            MATCH (d:Document)-[:CONTAINS]->(c:Concept)
            WHERE LOWER(c.name) CONTAINS '{safe_слово}'
            RETURN d.title AS документ, c.name AS концепт
            ORDER BY d.title
        """)

        while result.has_next():
            row = result.get_next()
            doc, concept = row[0], row[1]
            if doc not in знайдено:
                знайдено[doc] = []
            if concept not in знайдено[doc]:
                знайдено[doc].append(concept)

    if not знайдено:
        return ""

    # Формуємо читабельний контекст
    рядки = ["КОНТЕКСТ З БАЗИ ЗНАНЬ:"]
    for документ, концепти in знайдено.items():
        рядки.append(f"\nДокумент: {документ}")
        рядки.append(f"Теми: {', '.join(концепти[:8])}")  # максимум 8 концептів

    return "\n".join(рядки)


