    концепти = документ['концепти']

    # Додаємо документ
    safe_назва = назва.replace("'", "''")
    conn.execute(f"""
        MERGE (:Document {{id: '{safe_назва}', title: '{safe_назва}'}})
    """)

    # Додаємо кожен концепт і зв'язок документ→концепт
