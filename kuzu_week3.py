def очисти_концепти(список_рядків, мін_довжина=3, макс_довжина=35):
    """
    Прибирає непотрібні рядки:
    - занадто короткі (менше мін_довжина символів)
    - занадто довгі (більше макс_довжина — це речення, не концепт)
    - дублікати
    - рядки з URL, IP адресами, апострофами
    """
    очищені = []
    бачені = set()  # set — колекція унікальних значень

    for рядок in список_рядків:
        # Прибираємо зайві пробіли
        рядок = рядок.strip()

        # Пропускаємо якщо:
        if len(рядок) < мін_довжина:           continue  # занадто короткий
        if len(рядок) > макс_довжина:          continue  # занадто довгий
        if рядок.lower() in бачені:             continue  # дублікат
        if re.search(r'https?://', рядок):      continue  # URL
        if re.search(r'\d+\.\d+\.\d+', рядок): continue  # IP адреса
        if re.search(r'```', рядок):            continue  # код
        if "'" in рядок or "'" in рядок:        continue  # апостроф — ламає запит

        бачені.add(рядок.lower())
        очищені.append(рядок)

    return очищені

