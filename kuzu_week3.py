def пошук(запит: str) -> dict:
    """
    Шукає у Knowledge Graph документи пов'язані із запитом.
    Повертає: список документів і знайдених концептів.

    Це спрощений прообраз того що робить RAG:
    запит → пошук у базі → контекст для LLM
    """
    safe_запит = запит.replace("'", "''").lower()

    result = conn.execute(f"""
        MATCH (d:Document)-[:CONTAINS]->(c:Concept)
        WHERE LOWER(c.name) CONTAINS '{safe_запит}'
        RETURN d.title AS документ, c.name AS концепт
        ORDER BY d.title
    """)

    знайдені = {}
    while result.has_next():
        row = result.get_next()
        doc, concept = row[0], row[1]
        if doc not in знайдені:
            знайдені[doc] = []
        знайдені[doc].append(concept)

    return знайдені

