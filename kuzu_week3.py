def витягни_заголовки(текст):
    """
    Знаходить всі заголовки у Markdown файлі.
    ## Заголовок → ['Заголовок']
    """
    # re.findall шукає всі входження патерну
    # r'^#{1,3}\s+(.+)' означає:
    #   ^ — початок рядка
    #   #{1,3} — від 1 до 3 символів #
    #   \s+ — пробіл після #
    #   (.+) — захопити решту рядка (це і є заголовок)
    заголовки = re.findall(r'^#{1,3}\s+(.+)', текст, re.MULTILINE)
    return заголовки

def витягни_жирний_текст(текст):
    """
    Знаходить **жирний текст** — зазвичай це ключові терміни.
    """
    # \*\* — екранований символ * (бо * має особливе значення в regex)
    # (.+?) — захопити текст між ** та ** (? = мінімально)
    терміни = re.findall(r'\*\*(.+?)\*\*', текст)
    return терміни

def витягни_пункти_списку(текст):
    """
    Знаходить пункти списку: - пункт або * пункт
    """
    пункти = re.findall(r'^[-*]\s+(.+)', текст, re.MULTILINE)
    return пункти

