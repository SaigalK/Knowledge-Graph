def обробити_файл(шлях_до_файлу):
    """
    Читає файл і повертає словник:
    {
        'назва': 'назва файлу без .md',
        'концепти': ['концепт1', 'концепт2', ...]
    }
    """
    with open(шлях_до_файлу, encoding='utf-8') as f:
        текст = f.read()

    # Витягуємо з різних джерел
    всі_кандидати = []
    всі_кандидати += витягни_заголовки(текст)
    всі_кандидати += витягни_жирний_текст(текст)

    # Очищаємо
    концепти = очисти_концепти(всі_кандидати)

    # Назва документу = ім'я файлу без .md
    назва = os.path.basename(шлях_до_файлу).replace('.md', '')

    return {
        'назва': назва,
        'концепти': концепти
    }

